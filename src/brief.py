def cmd_picker(args) -> None:
    """Return numbered run list for interactive picker."""
    runs = _resolve_runs(args)
    result = _render_picker_list(runs, _now())
    if not result["runs"]:
        # No upcoming runs — show the next run after now
        all_runs = _load_runs(args)
        now = _now()
        future = [r for r in all_runs if r.scheduled >= now]
        future.sort(key=lambda r: r.scheduled)
        if future:
            result["next_run"] = {
                "scheduled": future[0].scheduled.isoformat(),
                "game": future[0].game,
                "category": future[0].category,
                "estimate": future[0].estimate,
            }
    print(json.dumps(result, indent=2))


