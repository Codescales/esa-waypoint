def cmd_cache_past_schedules(args) -> None:
    """Pre-fetch and cache all past Horaro schedules.

    The cached dump at output/briefs/.cache/past_schedules.json is
    used by `_horaro_past_appearances` to avoid fetching 40+
    schedules per runner. Run this once before a batch of
    runner-profile calls.
    """
    org = args.org or os.getenv("HORARO_ORG", "esa")
    current_slugs = set(_csv_env("HORARO_SCHEDULES", ""))
    pin = _csv_env("HORARO_PAST_SCHEDULES", "")
    all_schedules = _past_event_schedules(org, current_slugs)
    if pin:
        pin_set = set(pin)
        all_schedules = [s for s in all_schedules if s.get("slug") in pin_set]

    cache: dict[str, dict] = {}
    errors: list[str] = []
    for s in all_schedules:
        slug = s.get("slug", "")
        try:
            sched = fetch_schedule(org, slug)
            items = []
            for it in sched.items:
                items.append({
                    "game": it.game,
                    "players": it.players,
                    "category": it.category,
                    "scheduled": it.scheduled.isoformat(),
                    "estimate": it.estimate_str,
                    "stream": it.stream,
                })
            cache[slug] = {
                "name": sched.name,
                "start": sched.start.isoformat(),
                "items": items,
            }
        except Exception as e:
            errors.append(f"{slug}: {type(e).__name__}: {e}")
            cache[slug] = {
                "name": s.get("name", ""),
                "start": s.get("start", ""),
                "items": [],
                "error": str(e),
            }

    os.makedirs(CACHE_DIR, exist_ok=True)
    path = os.path.join(CACHE_DIR, "past_schedules.json")
    with open(path, "w") as f:
        json.dump(cache, f, indent=2)

    result = {
        "cached": len(cache),
        "total_items": sum(len(v.get("items", [])) for v in cache.values()),
        "errors": errors,
        "path": path,
    }
    print(json.dumps(result, indent=2))


