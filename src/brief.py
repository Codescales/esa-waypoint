def _now() -> datetime:
    return datetime.now(TZ)


def _load_developer_cache() -> dict[str, str]:
    os.makedirs(CACHE_DIR, exist_ok=True)
    if not os.path.exists(DEVELOPER_CACHE_PATH):
        return {}
    try:
        with open(DEVELOPER_CACHE_PATH) as f:
            data = json.load(f)
            if not isinstance(data, dict):
                return {}
            return data
    except (json.JSONDecodeError, OSError):
        # Corrupt cache — back it up and start fresh
        backup = DEVELOPER_CACHE_PATH + ".corrupt-" + _now().strftime("%Y%m%dT%H%M%S")
        try:
            os.rename(DEVELOPER_CACHE_PATH, backup)
        except OSError:
            pass
        return {}


def _save_developer_cache(cache: dict[str, str]) -> None:
    os.makedirs(CACHE_DIR, exist_ok=True)
    with open(DEVELOPER_CACHE_PATH, "w") as f:
        json.dump(cache, f, indent=2)


# ── past-event cache ────────────────────────────────────────────────────────
# Caches the list of past Horaro slugs (refreshed when missing or older
# than 24h). The list is small and stable so re-listing is cheap; we
# only avoid hitting horaro on every brief in a batch run.


def _load_past_events_cache() -> dict:
    """Return cached past-event metadata: {fetched_at, schedules: [...]}."""
    if not os.path.exists(PAST_EVENTS_CACHE_PATH):
        return {}
    try:
        with open(PAST_EVENTS_CACHE_PATH) as f:
            data = json.load(f)
            if not isinstance(data, dict):
                return {}
            return data
    except (json.JSONDecodeError, OSError):
        return {}


def _save_past_events_cache(schedules: list[dict]) -> None:
    os.makedirs(CACHE_DIR, exist_ok=True)
    payload = {
        "fetched_at": _now().isoformat(),
        "schedules": schedules,
    }
    with open(PAST_EVENTS_CACHE_PATH, "w") as f:
        json.dump(payload, f, indent=2)


def _past_event_schedules(org: str, exclude_slugs: set[str], max_age_hours: float = 24) -> list[dict]:
    """Return all Horaro schedules under `org` minus `exclude_slugs`.

    Results are cached on disk for `max_age_hours` so a 200-run batch
    doesn't hammer horaro.net. The list is small (~50 events for ESA)
    so a single re-fetch per day is cheap.
    """
    cache = _load_past_events_cache()
    fetched_at = cache.get("fetched_at")
    schedules = cache.get("schedules", [])
    fresh = False
    if fetched_at:
        try:
            age = _now() - datetime.fromisoformat(fetched_at)
            fresh = age.total_seconds() < max_age_hours * 3600
        except ValueError:
            fresh = False
    if not fresh or not schedules:
        try:
            schedules = list_event_schedules(org)
            _save_past_events_cache(schedules)
        except Exception:
            # Fall back to whatever's in the cache (even if stale)
            schedules = cache.get("schedules", [])
    return [s for s in schedules if s.get("slug") and s["slug"] not in exclude_slugs]


# ── lineup.esamarathon.dev cache ─────────────────────────────────────────────
# This is the marathon's own attendee tracker — authoritative for
# past-ESA counts, twitch handles, and country. The full payload
# (~360KB, ~2,800 people) is cached on disk for 24h. Per-runner lookups
# happen in-memory against the cached payload.


def _load_lineup_cache() -> dict:
    """Return the cached lineup payload, refreshing if stale or missing.

    Schema:
        {
          "fetched_at": ISO timestamp,
          "people": [...],         # raw lineup records
          "by_twitch": {twitch_lower: [indices]},
          "by_name":   {name_lower:  [indices]},
        }
    """
    os.makedirs(CACHE_DIR, exist_ok=True)
    if os.path.exists(LINEUP_CACHE_PATH):
        try:
            with open(LINEUP_CACHE_PATH) as f:
                data = json.load(f)
            if isinstance(data, dict) and data.get("people"):
                fetched_at = data.get("fetched_at")
                if fetched_at:
                    try:
                        age = _now() - datetime.fromisoformat(fetched_at)
                        if age.total_seconds() < LINEUP_TTL_HOURS * 3600:
                            return data
                    except ValueError:
                        pass
        except (json.JSONDecodeError, OSError):
            pass

    # Fetch fresh from lineup.esamarathon.dev
    import urllib.request
    try:
        req = urllib.request.Request(
            LINEUP_API_URL,
            headers={"User-Agent": "esa-brief-skill/0.1 (host briefing tool)"},
        )
        with urllib.request.urlopen(req, timeout=30) as resp:  # nosec B310
            payload = json.loads(resp.read().decode("utf-8"))
    except Exception as e:
        # Network down — try the stale cache as a last resort
        if os.path.exists(LINEUP_CACHE_PATH):
            try:
                with open(LINEUP_CACHE_PATH) as f:
                    data = json.load(f)
                if isinstance(data, dict) and data.get("people"):
                    return data
            except (json.JSONDecodeError, OSError):
                pass
        return {"fetched_at": _now().isoformat(), "people": [],
                "by_twitch": {}, "by_name": {}, "error": str(e)}

    people = payload.get("people", []) or []
    by_twitch: dict[str, list[int]] = {}
    by_name: dict[str, list[int]] = {}
    for i, p in enumerate(people):
        tw = (p.get("twitch") or "").strip().lower()
        if tw:
            by_twitch.setdefault(tw, []).append(i)
        nm = (p.get("name") or "").strip().lower()
        if nm:
            by_name.setdefault(nm, []).append(i)

    out = {
        "fetched_at": _now().isoformat(),
        "people": people,
        "by_twitch": by_twitch,
        "by_name": by_name,
    }
    try:
        with open(LINEUP_CACHE_PATH, "w") as f:
            json.dump(out, f, indent=2)
    except OSError:
        # Cache is best-effort; we still return the in-memory payload
        pass
    return out


def _lineup_lookup(lineup: dict, name: str, twitch: str) -> list[dict]:
    """Return all lineup records matching name+twitch (OR semantics).

    Twitch match wins when both are present — the lineup is keyed
    primarily by twitch. A name-only match is returned as a fallback
    for runners without a twitch handle on the lineup.
    """
    people = lineup.get("people", [])
    by_twitch = lineup.get("by_twitch", {})
    by_name = lineup.get("by_name", {})
    name_lower = (name or "").strip().lower()
    twitch_lower = (twitch or "").strip().lower()

    if twitch_lower and twitch_lower in by_twitch:
        return [people[i] for i in by_twitch[twitch_lower]]
    if name_lower and name_lower in by_name:
        return [people[i] for i in by_name[name_lower]]
    return []


