def _runner_profile_is_fresh(entry: dict) -> bool:
    """True if a cached profile is inside the TTL window."""
    fetched_at = entry.get("fetched_at")
    if not fetched_at:
        return False
    try:
        age = _now() - datetime.fromisoformat(fetched_at)
    except ValueError:
        return False
    return age.total_seconds() < RUNNER_PROFILE_TTL_HOURS * 3600


def _extract_tenure(signup: str | None) -> dict:
    """Compute streaming tenure from an SRC `signup` ISO timestamp.

    Returns years (int), years_label ("5 years"), display_date
    (YYYY-MM-DD), and an "unavailable" flag if signup is missing.
    """
    if not signup:
        return {
            "available": False,
            "signup": None,
            "years": None,
            "label": "Not found — ask the runner.",
        }
    try:
        signup_dt = datetime.fromisoformat(signup.replace("Z", "+00:00"))
    except ValueError:
        return {
            "available": False,
            "signup": signup,
            "years": None,
            "label": "Not found — ask the runner.",
        }
    now = _now()
    if signup_dt.tzinfo is not None:
        now = now.replace(tzinfo=signup_dt.tzinfo)
    delta_days = (now - signup_dt).days
    years = max(0, delta_days // 365)
    return {
        "available": True,
        "signup": signup_dt.date().isoformat(),
        "years": years,
        "label": f"{years} year{'s' if years != 1 else ''} on speedrun.com (since {signup_dt.date().isoformat()})",
    }


def _extract_communities(aboutme: str | None, twitch_bio: str | None) -> list[dict]:
    """Scan SRC aboutme + Twitch bio for community markers.

    Returns a list of {name, source, evidence}. Each match is anchored
    by a literal pattern from KNOWN_COMMUNITIES; the evidence line
    shows the matched substring (truncated).
    """
    sources: list[tuple[str, str | None]] = [
        ("speedrun.com", aboutme or ""),
        ("twitch", twitch_bio or ""),
    ]
    found: list[dict] = []
    seen: set[tuple[str, str]] = set()
    for source_name, text in sources:
        if not text:
            continue
        text_lower = text.lower()
        for community in KNOWN_COMMUNITIES:
            for pat in community["patterns"]:
                m = re.search(pat, text_lower, re.IGNORECASE)
                if not m:
                    continue
                key = (source_name, community["name"])
                if key in seen:
                    continue
                seen.add(key)
                # Pull a small window of the original text for evidence.
                start = max(0, m.start() - 20)
                end = min(len(text), m.end() + 30)
                evidence = text[start:end].strip()
                if len(evidence) > 80:
                    evidence = evidence[:77] + "..."
                found.append({
                    "name": community["name"],
                    "source": source_name,
                    "evidence": evidence,
                })
                break  # one match per community per source is enough
    return found


def _render_picker_list(runs: list[xr.RunRow], now: datetime) -> dict:
    """Build the interactive picker JSON response."""
    result = {
        "now_stockholm": now.isoformat(),
        "runs": [],
    }
    for r in runs:
        status = "upcoming"
        if r.scheduled < now and (now - r.scheduled).total_seconds() < 7200:
            status = "recently-completed"
        result["runs"].append({
            "pick": r.pick,
            "scheduled": r.scheduled.isoformat(),
            "status": status,
            "game": r.game,
            "category": r.category,
            "estimate": r.estimate,
            "runner": r.runner_display,
            "stream": xr.stream_token_to_short(r.stream),
        })
    return result


def _load_runs(args) -> list[xr.RunRow]:
    """Load runs from the configured source (xlsx or db)."""
    source = getattr(args, "source", "xlsx") or "xlsx"
    if source == "db":
        db_path = getattr(args, "db_path", "") or "output/esa.db"
        return xr.read_cross_reference_from_db(db_path)
    return xr.read_cross_reference(args.spreadsheet)


def _load_incentives(args) -> list[xr.IncentiveRow]:
    """Load incentives from the configured source (xlsx or db)."""
    source = getattr(args, "source", "xlsx") or "xlsx"
    if source == "db":
        db_path = getattr(args, "db_path", "") or "output/esa.db"
        return xr.read_incentives_from_db(db_path)
    return xr.read_incentives(args.spreadsheet)


def _resolve_runs(
    args,
    max_upcoming: int = 50,
) -> list[xr.RunRow]:
    """Load runs from configured source and apply filters."""
    all_runs = _load_runs(args)
    now = _now()

    stream_token = getattr(args, "stream", "")
    if stream_token:
        all_runs = xr.filter_runs_by_stream(all_runs, stream_token)

    if getattr(args, "marathon", False):
        return all_runs

    if getattr(args, "window", None):
        start_str, end_str = args.window.split("/")
        start = datetime.fromisoformat(start_str)
        end = datetime.fromisoformat(end_str)
        return xr.filter_runs_by_window(all_runs, start, end)

    next_hours = getattr(args, "next_hours", 0)
    if next_hours:
        start = now
        end = now + timedelta(hours=next_hours)
        return xr.filter_runs_by_window(all_runs, start, end)

    # Default: upcoming only, capped
    upcoming = [r for r in all_runs if not r.scheduled < now - timedelta(hours=2)]
    upcoming.sort(key=lambda r: r.scheduled)
    return upcoming[:max_upcoming]


