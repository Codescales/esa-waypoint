def cmd_research(args) -> None:
    """Fetch SRC categories + records + WR deltas for a game."""
    game_name = args.game
    current_category = args.category or ""
    estimate_min = args.estimate_min or 0

    result = {
        "game": game_name,
        "category": current_category,
        "srcext_game": {},
        "categories": [],
        "records": [],
        "estimate_seconds": estimate_min * 60,
    }

    game = search_src_game(game_name)
    if not game:
        print(json.dumps(result, indent=2))
        return

    game_id = game["id"]
    abbr = game.get("abbreviation", "")
    src_url = f"https://www.speedrun.com/{abbr}" if abbr else ""

    result["srcext_game"] = {
        "id": game_id,
        "name": (game.get("names", {}) or {}).get("international", ""),
        "abbreviation": abbr,
        "src_url": src_url,
    }

    try:
        cats = fetch_src_categories(game_id)
        result["categories"] = [
            {
                "id": c["id"],
                "name": c["name"],
                "type": c.get("type", ""),
            }
            for c in cats
        ]
    except SrcApiError:
        result["category_error"] = "Failed to fetch categories"

    try:
        records = fetch_game_records(game_id)
        result["records"] = records
    except SrcApiError:
        result["record_error"] = "Failed to fetch records"

    print(json.dumps(result, indent=2))


def cmd_research_runner(args) -> None:
    """Look up runner profile on SRC with tiered verification.

    Extended output (still backwards compatible — new keys are additive):
      - `signup`, `aboutme`, `location`: raw fields from /users/{id}
      - `tenure`: derived years-on-SRC dict (see _extract_tenure)
    """
    twitch = args.twitch or ""
    name = args.name or ""

    result = {
        "verified": False,
        "confidence": None,
        "profile": None,
        "personal_bests": [],
        "twitch_matched": False,
        "name_matched": False,
        "src_url": None,
        "signup": None,
        "aboutme": None,
        "location": None,
        "tenure": {"available": False, "signup": None, "years": None,
                    "label": "Not found — ask the runner."},
    }

    try:
        user = search_user_by_lookup(twitch) or search_user_by_lookup(name)
    except SrcApiError:
        print(json.dumps(result, indent=2))
        return
    if not user:
        print(json.dumps(result, indent=2))
        return

    user_id = user.get("id", "")
    user_name = (user.get("names", {}) or {}).get("international", "")
    user_twitch_uri = (user.get("twitch", {}) or {}).get("uri", "") if user.get("twitch") else ""
    src_url = f"https://www.speedrun.com/users/{user_name}" if user_name else None

    # Tiered verification
    twitch_matched = bool(twitch and twitch.lower() in user_twitch_uri.lower())
    name_matched = bool(name and user_name.lower() == name.lower())

    confidence = None
    if twitch_matched:
        confidence = "high"
    elif name_matched:
        confidence = "moderate"
    else:
        # Discard — can't verify
        print(json.dumps(result, indent=2))
        return

    result["verified"] = True
    result["confidence"] = confidence
    result["profile"] = {
        "id": user_id,
        "display_name": user_name,
        "weblink": user.get("weblink"),
        "twitch_uri": user_twitch_uri,
    }
    result["twitch_matched"] = twitch_matched
    result["name_matched"] = name_matched
    result["src_url"] = src_url

    # Pull extra fields (signup, aboutme, location) from /users/{id}.
    # The lookup response can omit these, so we make a second call.
    profile = None
    try:
        profile = fetch_user_profile(user_id)
    except SrcApiError:
        profile = None
    if profile:
        result["signup"] = profile.get("signup")
        aboutme = profile.get("aboutme")
        if aboutme and isinstance(aboutme, str):
            # Trim to keep the JSON small — full bio is rarely needed.
            result["aboutme"] = aboutme if len(aboutme) <= 1000 else aboutme[:997] + "..."
        loc = profile.get("location")
        if isinstance(loc, dict):
            country = ((loc.get("country") or {}).get("names") or {}).get("international", "")
            region = ((loc.get("region") or {}).get("names") or {}).get("international", "")
            if country or region:
                result["location"] = ", ".join(p for p in (region, country) if p)

    result["tenure"] = _extract_tenure(result["signup"])

    try:
        pbs = fetch_user_personal_bests(user_id)
        seen_games: dict[str, dict] = {}
        for pb in pbs:
            gname = pb["game_name"]
            if gname not in seen_games:
                seen_games[gname] = {
                    "game_name": gname,
                    "game_abbr": pb["game_abbr"],
                    "times": [],
                }
            seen_games[gname]["times"].append({
                "time_seconds": pb["time_seconds"],
            })
        result["personal_bests"] = list(seen_games.values())
    except (SrcApiError, Exception):
        pass

    print(json.dumps(result, indent=2))


def _csv_env(name: str, default: str = "") -> list[str]:
    """Return a comma-separated env var as a list of stripped values."""
    raw = os.getenv(name, default) or ""
    return [v.strip() for v in raw.split(",") if v.strip()]


def _first_event_year(events: list[str]) -> int | None:
    """Extract the earliest 4-digit year from lineup event labels.

    Lineup events look like "ESA 2017", "ESA Summer 2022",
    "ESA Winter 2018". Pulls the first 19xx/20xx year found.
    """
    if not events:
        return None
    years: list[int] = []
    for e in events:
        m = re.search(r"\b((?:19|20)\d{2})\b", e or "")
        if m:
            years.append(int(m.group(1)))
    return min(years) if years else None


def _horaro_past_appearances(name: str, twitch: str, org: str = "") -> dict:
    """Return {events, matches, first_appearance, error} from Horaro only.

    Used as a fallback when lineup.esamarathon.dev has no record.
    Walks the org's past Horaro schedules (excluding the current
    marathon's slugs) and matches players cell by name + twitch.

    Uses a pre-cached schedule dump at
    `output/briefs/.cache/past_schedules.json` (generated by
    `python3 -m src.brief cache-past-schedules`) to avoid fetching
    40+ schedules per runner. Falls back to live fetch if the cache
    is missing.
    """
    org = org or os.getenv("HORARO_ORG", "esa")
    current_slugs = set(_csv_env("HORARO_SCHEDULES", ""))
    pin = _csv_env("HORARO_PAST_SCHEDULES", "")
    all_schedules = _past_event_schedules(org, current_slugs)
    if pin:
        pin_set = set(pin)
        all_schedules = [s for s in all_schedules if s.get("slug") in pin_set]

    # Load pre-cached schedule data if available
    past_cache_path = os.path.join(CACHE_DIR, "past_schedules.json")
    past_cache: dict = {}
    if os.path.exists(past_cache_path):
        try:
            with open(past_cache_path) as f:
                past_cache = json.load(f)
        except (json.JSONDecodeError, OSError):
            past_cache = {}

    matches: list[dict] = []
    events: set[str] = set()
    first_appearance: dict | None = None
    error: str | None = None

    for sched in all_schedules:
        slug = sched.get("slug", "")
        cached = past_cache.get(slug)
        if cached and cached.get("items"):
            items = cached["items"]
            for item in items:
                if not _item_mentions_runner(item, name, twitch):
                    continue
                events.add(slug)
                entry = {
                    "event_slug": slug,
                    "event_name": cached.get("name", sched.get("name", "")),
                    "event_start": cached.get("start", sched.get("start", "")),
                    "scheduled": item.get("scheduled", ""),
                    "game": item.get("game", ""),
                    "category": item.get("category", ""),
                    "estimate": item.get("estimate", ""),
                    "stream": item.get("stream", ""),
                    "match": "twitch" if (twitch and twitch.lower() in (item.get("players", "") or "").lower()) else "name",
                }
                matches.append(entry)
                if first_appearance is None or entry["scheduled"] < first_appearance["scheduled"]:
                    first_appearance = entry
        else:
            # Live fetch fallback
            try:
                schedule = fetch_schedule(org, slug)
            except Exception as e:
                error = f"horaro fetch failed: {type(e).__name__}: {e}"
                continue
            for item in schedule.items:
                if not item.mentions_runner(name, twitch):
                    continue
                events.add(slug)
                entry = {
                    "event_slug": slug,
                    "event_name": sched.get("name", ""),
                    "event_start": sched.get("start", ""),
                    "scheduled": item.scheduled.isoformat(),
                    "game": item.game,
                    "category": item.category,
                    "estimate": item.estimate_str,
                    "stream": item.stream,
                    "match": "twitch" if (twitch and twitch.lower() in (item.players or "").lower()) else "name",
                }
                matches.append(entry)
                if first_appearance is None or item.scheduled.isoformat() < first_appearance["scheduled"]:
                    first_appearance = entry

    matches.sort(key=lambda m: m.get("scheduled", ""))
    return {
        "events": sorted(events),
        "matches": matches,
        "first_appearance": first_appearance,
        "error": error,
    }


def _item_mentions_runner(item: dict, name: str, twitch: str) -> bool:
    """Check if a cached schedule item mentions the runner.

    Mirrors ScheduleItem.mentions_runner but operates on the
    pre-cached dict format (game, players, category, ...).
    """
    if not name and not twitch:
        return False
    name_lower = (name or "").strip().lower()
    twitch_lower = (twitch or "").strip().lower()
    players = item.get("players", "") or ""
    players_lower = players.lower()
    # Check markdown link names
    for raw in re.findall(r"\[([^\]]+)\]\([^)]+\)", players):
        if name_lower and raw.strip().lower() == name_lower:
            return True
    # Check word-boundary substring
    if name_lower and re.search(rf"\b{re.escape(name_lower)}\b", players_lower):
        return True
    # Check twitch URL
    if twitch_lower and twitch_lower in players_lower:
        return True
    return False


def cmd_past_esa(args) -> None:
    """Find every past ESA event where this runner appeared.

    Primary source: lineup.esamarathon.dev (authoritative counts and
    event list, refreshed every 24h on disk). Fallback: horaro.net
    schedule scan (slower, finds events lineup doesn't track). The
    response marks which sources contributed so the brief can cite
    them honestly.
    """
    name = args.name or ""
    twitch = args.twitch or ""
    org = args.org or os.getenv("HORARO_ORG", "esa")

    sources_used: list[str] = []
    lineup_records = []
    lineup_error: str | None = None
    try:
        lineup = _load_lineup_cache()
        lineup_records = _lineup_lookup(lineup, name, twitch)
    except Exception as e:
        lineup_error = f"{type(e).__name__}: {e}"
        lineup_records = []

    lineup_record = lineup_records[0] if lineup_records else None
    lineup_events: list[str] = list(lineup_record.get("events", [])) if lineup_record else []
    lineup_runs: int = int(lineup_record.get("runs", 0)) if lineup_record else 0
    lineup_country: str = lineup_record.get("country", "") if lineup_record else ""

    if lineup_record:
        sources_used.append("lineup.esamarathon.dev")
    else:
        if lineup_error:
            sources_used.append(f"lineup.esamarathon.dev (error: {lineup_error})")

    # Horaro fallback — always run it for per-run detail (game/category)
    # even when lineup is the primary source for counts.
    horaro = _horaro_past_appearances(name, twitch, org)
    if horaro["events"]:
        if "horaro.net" not in sources_used:
            sources_used.append("horaro.net")
        if horaro.get("error"):
            sources_used.append(f"horaro.net (error: {horaro['error']})")

    # Merge: use lineup's event list as the canonical "events attended",
    # but use horaro's per-run matches to build a game+category timeline.
    # event_count is whichever source is authoritative.
    events_attended = lineup_events if lineup_record else horaro["events"]
    event_count = len(events_attended)
    first_event_year = _first_event_year(events_attended)

    # Appearance count: lineup's `runs` field is the authoritative
    # submission count; horaro's `matches` count is a per-schedule-item
    # tally which is usually lower (one row per submission slot).
    if lineup_record:
        appearance_count = lineup_runs
        first_appearance = {
            "event_slug": "",
            "event_name": events_attended[0] if events_attended else "",
            "event_start": "",
            "scheduled": "",
            "game": "",
            "category": "",
            "estimate": "",
            "stream": "",
            "match": "lineup",
        } if events_attended else None
    else:
        appearance_count = len(horaro["matches"])
        first_appearance = horaro["first_appearance"]

    # Fill first_appearance game/category from horaro if lineup-only
    if lineup_record and horaro["matches"] and first_appearance:
        # Find the earliest horaro match
        earliest = horaro["matches"][0] if horaro["matches"] else None
        if earliest:
            first_appearance["game"] = earliest.get("game", "")
            first_appearance["category"] = earliest.get("category", "")
            first_appearance["estimate"] = earliest.get("estimate", "")
            first_appearance["scheduled"] = earliest.get("scheduled", "")
            first_appearance["event_slug"] = earliest.get("event_slug", "")
            first_appearance["event_start"] = earliest.get("event_start", "")
            first_appearance["stream"] = earliest.get("stream", "")

    # ── ESA runs aggregation ──
    # Count how many times each game+category appears in horaro matches.
    esa_games: dict[str, dict] = {}
    for m in horaro["matches"]:
        g = m.get("game", "") or ""
        cat = m.get("category", "") or ""
        # Strip markdown links from game name for cleaner display
        clean_game = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", g).strip()
        key = f"{clean_game}|{cat}"
        if key not in esa_games:
            esa_games[key] = {
                "game": clean_game,
                "category": cat,
                "count": 0,
                "events": [],
            }
        esa_games[key]["count"] += 1
        ev = m.get("event_slug", "") or m.get("event_name", "") or ""
        if ev and ev not in esa_games[key]["events"]:
            esa_games[key]["events"].append(ev)

    esa_runs_list = sorted(esa_games.values(), key=lambda x: -x["count"])
    esa_game_count = len(set(g["game"] for g in esa_runs_list))
    esa_category_count = len(esa_runs_list)

    result = {
        "name": name,
        "twitch": twitch,
        "event_count": event_count,
        "appearance_count": appearance_count,
        "first_event": events_attended[0] if events_attended else None,
        "first_event_year": first_event_year,
        "events": events_attended,
        "first_appearance": first_appearance,
        "matches": horaro["matches"],
        "esa_runs": esa_runs_list,
        "esa_game_count": esa_game_count,
        "esa_category_count": esa_category_count,
        "country": lineup_country,
        "country_name": _country_name(lineup_country) if lineup_country else "",
        "sources_used": sources_used,
        "lineup_url": "https://lineup.esamarathon.dev/api/public" if lineup_record else None,
    }
    print(json.dumps(result, indent=2))


def _compose_stats(
    *,
    name: str,
    twitch: str,
    past_esa: dict,
    tenure: dict,
    communities: list[dict],
    personal_bests: list[dict],
    src_url: str | None,
    verified: bool,
    confidence: str | None,
) -> dict:
    """Build the {summary, stats, sources, errors} object for a runner.

    Pure: takes already-fetched data. Used by `cmd_runner_profile`
    and `sync_runner_profiles_to_db`. `errors` is a
    list[str] for any field that was unavailable; `summary` always
    carries every headline number (None when not available) so the
    web viewer can render placeholders.
    """
    pbs = personal_bests or []
    game_count = len(pbs)
    category_count = sum(len(g.get("times", [])) for g in pbs)
    top_games = [g.get("game_name", "") for g in pbs[:5] if g.get("game_name")]

    country = past_esa.get("country", "") or ""
    country_name = past_esa.get("country_name", "") or ""
    first_event = past_esa.get("first_event") or ""
    first_event_year = past_esa.get("first_event_year")
    years_active = None
    if first_event_year:
        years_active = max(0, _now().year - first_event_year)

    esa_runs = past_esa.get("esa_runs", [])
    esa_game_count = past_esa.get("esa_game_count", 0)
    esa_category_count = past_esa.get("esa_category_count", 0)
    esa_top_games = list(dict.fromkeys(g["game"] for g in esa_runs))[:5]

    summary = {
        "event_count": past_esa.get("event_count", 0),
        "appearance_count": past_esa.get("appearance_count", 0),
        "first_event": first_event or None,
        "first_event_year": first_event_year,
        "years_active": years_active,
        "pb_count": category_count,
        "pb_game_count": game_count,
        "src_tenure_years": tenure.get("years") if tenure.get("available") else None,
        "community_count": len(communities),
        "country_code": country.upper() if country else None,
        "country_name": country_name or None,
        "esa_game_count": esa_game_count,
        "esa_category_count": esa_category_count,
    }

    stats = {
        "past_esa": {
            "event_count": past_esa.get("event_count", 0),
            "appearance_count": past_esa.get("appearance_count", 0),
            "first_event": first_event or None,
            "first_event_year": first_event_year,
            "events": past_esa.get("events", []),
            "first_appearance": past_esa.get("first_appearance"),
            "esa_runs": esa_runs,
            "esa_game_count": esa_game_count,
            "esa_category_count": esa_category_count,
            "esa_top_games": esa_top_games,
            "sources": past_esa.get("sources_used", []),
            "verified": bool(past_esa.get("event_count", 0) or past_esa.get("appearance_count", 0)),
        },
        "tenure": tenure,
        "communities": communities,
        "country": {
            "code": country.upper() if country else None,
            "name": country_name or None,
            "source": "lineup.esamarathon.dev" if country else None,
            "confidence": "unverified" if country else None,
        } if country else {"code": None, "name": None, "source": None, "confidence": None},
        "personal_bests": {
            "game_count": game_count,
            "category_count": category_count,
            "top_games": top_games,
        },
    }

    sources: list[dict] = []
    if past_esa.get("lineup_url"):
        sources.append({"name": "lineup.esamarathon.dev", "url": past_esa["lineup_url"]})
    if "horaro.net" in (past_esa.get("sources_used") or []):
        org = os.getenv("HORARO_ORG", "esa")
        sources.append({"name": "horaro.net", "url": f"https://horaro.net/{org}"})
    if src_url:
        sources.append({"name": "speedrun.com", "url": src_url})

    errors: list[str] = []
    if not stats["past_esa"]["verified"]:
        errors.append("past_esa: no record in lineup or horaro")
    if not tenure.get("available"):
        errors.append("tenure: SRC signup unavailable")
    if not communities:
        errors.append("communities: no community markers found in SRC aboutme")
    if not pbs:
        errors.append("personal_bests: no SRC PBs found")

    return {
        "summary": summary,
        "stats": stats,
        "sources": sources,
        "errors": errors,
        "verified": verified,
        "confidence": confidence,
    }


def _fetch_runner_profile(
    name: str,
    twitch: str,
    *,
    use_cache: bool = True,
) -> dict:
    """Build the composite profile for one runner.

    Pulls past ESA from lineup (horaro fallback), tenure + communities
    + PBs from speedrun.com. Caches the result to disk for
    RUNNER_PROFILE_TTL_HOURS unless `use_cache` is False. Errors are
    captured per-source and returned in the `errors` list so the
    caller can decide how to render them.
    """
    cache = _load_runner_profile_cache()
    key = _runner_profile_key(twitch, name)
    cached = cache.get(key) if use_cache else None
    if cached and _runner_profile_is_fresh(cached):
        # Return a deep copy so the caller can mutate freely
        return json.loads(json.dumps(cached))

    # ── past ESA: prefer lineup, fall back to horaro ──
    pa_args = argparse.Namespace(name=name, twitch=twitch, org="")
    past_esa: dict = {}
    try:
        import io
        from contextlib import redirect_stdout
        buf = io.StringIO()
        with redirect_stdout(buf):
            cmd_past_esa(pa_args)
        past_esa = json.loads(buf.getvalue())
    except Exception as e:
        past_esa = {
            "event_count": 0, "appearance_count": 0,
            "first_event": None, "first_event_year": None,
            "events": [], "first_appearance": None, "matches": [],
            "country": "", "country_name": "", "sources_used": [],
            "lineup_url": None, "error": str(e),
        }

    # ── SRC: tenure, aboutme, PBs ──
    src: dict = {}
    rr_args = argparse.Namespace(twitch=twitch, name=name)
    try:
        import io
        from contextlib import redirect_stdout
        buf = io.StringIO()
        with redirect_stdout(buf):
            cmd_research_runner(rr_args)
        src = json.loads(buf.getvalue())
    except Exception:
        src = {"verified": False, "tenure": {"available": False},
               "personal_bests": [], "aboutme": None, "src_url": None,
               "confidence": None}

    tenure = src.get("tenure") or {"available": False, "label": "Not found — ask the runner."}
    communities = _extract_communities(src.get("aboutme"), None)

    profile = _compose_stats(
        name=name,
        twitch=twitch,
        past_esa=past_esa,
        tenure=tenure,
        communities=communities,
        personal_bests=src.get("personal_bests", []),
        src_url=src.get("src_url"),
        verified=src.get("verified", False),
        confidence=src.get("confidence"),
    )
    profile["name"] = name
    profile["twitch"] = twitch
    profile["fetched_at"] = _now().isoformat()

    # Persist to disk cache (best-effort)
    cache[key] = profile
    _save_runner_profile_cache(cache)
    return profile


def cmd_runner_profile(args) -> None:
    """Compose past-ESA + tenure + communities + PBs into one JSON.

    This is the primary entry point for the runner-brief workflow.
    Each field is filled from a deterministic source (lineup +
    horaro for past events, SRC /users/{id} for tenure and aboutme,
    SRC personal-bests for the games list). When a field cannot be
    filled, the corresponding key carries an `unavailable: true` flag
    so the brief can mark "not found — ask the runner" rather than
    fabricating data.

    Honors a 7-day on-disk cache so a 200-run batch doesn't re-fetch
    lineup/SRC for every runner. `--refresh` forces a recompute.
    """
    name = args.name or ""
    twitch = args.twitch or ""
    use_cache = not getattr(args, "refresh", False)

    profile = _fetch_runner_profile(name, twitch, use_cache=use_cache)
    print(json.dumps(profile, indent=2))


